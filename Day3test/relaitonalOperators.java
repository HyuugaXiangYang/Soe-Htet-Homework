package Day3test;

public class relaitonalOperators {

	public static void main(String[] args) {
		int x = 100;
		int y = 100;
		
		System.out.println(x == y); //print true
		System.out.println(x != y); //print false
		System.out.println(x >= y); //print true
		
		//relational operators will automatically print true or false result.
			
	}

}
